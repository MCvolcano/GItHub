package com.entity;

public class Dishes {
	private Integer d_id;
	private String d_name;
	private String d_cuisine_name;
	private Double d_price;
	private Double d_vip_price;
	private String d_image;
	
	public Integer getD_id() {
		return d_id;
	}
	public void setD_id(Integer d_id) {
		this.d_id = d_id;
	}
	public String getD_name() {
		return d_name;
	}
	public void setD_name(String d_name) {
		this.d_name = d_name;
	}
	public String getD_cuisine_name() {
		return d_cuisine_name;
	}
	public void setD_cuisine_name(String d_cuisine_name) {
		this.d_cuisine_name = d_cuisine_name;
	}
	public Double getD_price() {
		return d_price;
	}
	public void setD_price(Double d_price) {
		this.d_price = d_price;
	}
	public Double getD_vip_price() {
		return d_vip_price;
	}
	public void setD_vip_price(Double d_vip_price) {
		this.d_vip_price = d_vip_price;
	}
	public String getD_image() {
		return d_image;
	}
	public void setD_image(String d_image) {
		this.d_image = d_image;
	}
	
	
	
}
